function generateNextPhaseMilestones(
  mode: string,
  currentSongs: number,
  currentVault: number
): RoadmapMilestone[] {
  const now = new Date();
  const milestones: RoadmapMilestone[] = [];

  const addMonths = (date: Date, months: number) => {
    const d = new Date(date);
    d.setMonth(d.getMonth() + months);
    return d.toISOString().split('T')[0];
  };

  const nextSongTarget = Math.ceil((currentSongs + 5) / 5) * 5;
  const nextVaultTarget = currentVault < 1000 ? 1000 :
    currentVault < 5000 ? 5000 :
    currentVault < 10000 ? 10000 :
    Math.ceil(currentVault / 10000) * 10000 + 10000;

  milestones.push(
    {
      id: `auto-${Date.now()}-1`,
      title: `Release ${nextSongTarget} songs total`,
      date: addMonths(now, 3),
      category: 'release',
      status: 'planned',
      description: `Continue building your catalog to ${nextSongTarget} songs`,
      autoGenerated: true,
      priority: 'medium',
    },
    {
      id: `auto-${Date.now()}-2`,
      title: `Reach $${nextVaultTarget.toLocaleString()} in treasury`,
      date: addMonths(now, 6),
      category: 'financial',
      status: 'planned',
      description: `Scale treasury vault to $${nextVaultTarget.toLocaleString()}`,
      autoGenerated: true,
      linkedToEngine: true,
      priority: 'high',
    },
  );

  // Mode-specific milestone
  const nextMode = mode === 'survival' ? 'stable' :
    mode === 'stable' ? 'expansion' :
    mode === 'expansion' ? 'aggressive' : 'aggressive';

  if (mode !== 'aggressive') {
    milestones.push({
      id: `auto-${Date.now()}-3`,
      title: `Upgrade engine to ${nextMode} mode`,
      date: addMonths(now, 4),
      category: 'engine',
      status: 'planned',
      description: `Transition economic engine from ${mode} to ${nextMode} operating mode`,
      autoGenerated: true,
      linkedToEngine: true,
      priority: 'high',
    });
  }

  return milestones;
}

function generateProjections(
  monthlyRevenue: number,
  monthlyExpenses: number,
  mode: string,
  currentVault: number
): Array<{ month: string; revenue: number; expenses: number; profit: number }> {
  const projections: Array<{ month: string; revenue: number; expenses: number; profit: number }> = [];
  const now = new Date();

  // Growth rates by mode
  const growthRates: Record<string, number> = {
    survival: 0.02,
    stable: 0.05,
    expansion: 0.10,
    aggressive: 0.15,
    defense: 0.01,
  };

  const growthRate = growthRates[mode] || 0.05;
  let revenue = monthlyRevenue || 100;
  let expenses = monthlyExpenses || 50;

  for (let i = 0; i < 12; i++) {
    const date = new Date(now);
    date.setMonth(date.getMonth() + i + 1);

    revenue = Math.round(revenue * (1 + growthRate) * 100) / 100;
    expenses = Math.round(expenses * (1 + growthRate * 0.5) * 100) / 100; // Expenses grow slower

    projections.push({
      month: date.toISOString().slice(0, 7),
      revenue: Math.round(revenue * 100) / 100,
      expenses: Math.round(expenses * 100) / 100,
      profit: Math.round((revenue - expenses) * 100) / 100,
    });
  }

  return projections;
}

function extractNumberFromTitle(title: string): number | null {
  const match = title.match(/\$?([\d,]+)/);
  if (match) {
    return parseInt(match[1].replace(/,/g, ''), 10);
  }
  return null;
}

