/**
 * Generates news for a completed milestone.
 * Creates entry in both artistNews (artist section) and main newsArticles.
 */
async function generateMilestoneNews(artistId: number, milestone: RoadmapMilestone): Promise<void> {
  try {
    const [artist] = await db.select({
      name: users.firstName,
      lastName: users.lastName,
      bio: users.biography,
      genre: users.genres,
    }).from(users).where(eq(users.id, artistId)).limit(1);

    if (!artist) return;

    const artistName = `${artist.name || ''} ${artist.lastName || ''}`.trim();

    // Use the news event orchestrator for cross-posting
    enqueueNewsEvent({
      type: 'milestone_reached',
      artistId,
      artistName,
      artistBio: artist.bio || undefined,
      genre: Array.isArray(artist.genre) ? artist.genre[0] : undefined,
      details: {
        milestoneTitle: milestone.title,
        milestoneCategory: milestone.category,
        milestoneDescription: milestone.description,
        priority: milestone.priority || 'medium',
      },
    });

    console.log(`[Bridge] News event enqueued for milestone: "${milestone.title}" (artist ${artistId})`);
  } catch (error) {
    console.error(`[Bridge] Error generating milestone news for artist ${artistId}:`, error);
  }
}

