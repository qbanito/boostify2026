/**
 * Syncs economic engine data into the business plan projections.
 * Called after each economic cycle or on-demand.
 */
export async function syncEngineToBusinessPlan(artistId: number): Promise<void> {
  try {
    // Get economic profile and vault
    const [profile] = await db.select().from(artistEconomicProfile)
      .where(eq(artistEconomicProfile.artistId, artistId)).limit(1);
    const [vault] = await db.select().from(artistTreasuryVault)
      .where(eq(artistTreasuryVault.artistId, artistId)).limit(1);

    if (!profile || !vault) return;

    // Get or create business plan
    let [plan] = await db.select().from(artistBusinessPlans)
      .where(eq(artistBusinessPlans.artistId, artistId)).limit(1);

    if (!plan) {
      // Auto-create minimal business plan linked to engine
      const [artist] = await db.select({
        name: users.firstName,
        lastName: users.lastName,
      }).from(users).where(eq(users.id, artistId)).limit(1);

      const artistName = artist ? `${artist.name || ''} ${artist.lastName || ''}`.trim() : 'Artist';

      [plan] = await db.insert(artistBusinessPlans).values({
        artistId,
        businessName: `${artistName} Music`,
        linkedEngineMode: profile.operatingMode || 'stable',
        roadmapAutoUpdate: true,
        milestones: generateDefaultMilestones(profile.operatingMode || 'stable'),
        roadmapExecutionLog: [],
      }).returning();
    }

    // Update linked engine mode
    const totalVault = Number(vault.operationBalance || 0) +
      Number(vault.reserveBalance || 0) +
      Number(vault.growthBalance || 0) +
      Number(vault.defiBalance || 0);

    // Sync revenue data from vault transactions (last 30 days)
    const thirtyDaysAgo = new Date();
    thirtyDaysAgo.setDate(thirtyDaysAgo.getDate() - 30);

    const [txSums] = await db.select({
      totalIncome: sql<number>`COALESCE(SUM(CASE WHEN ${treasuryTransactions.type} = 'income' THEN ${treasuryTransactions.amount} ELSE 0 END), 0)`,
      totalExpense: sql<number>`COALESCE(SUM(CASE WHEN ${treasuryTransactions.type} = 'expense' THEN ${treasuryTransactions.amount} ELSE 0 END), 0)`,
    })
    .from(treasuryTransactions)
    .where(and(
      eq(treasuryTransactions.artistId, artistId),
      gte(treasuryTransactions.createdAt, thirtyDaysAgo)
    ));

    const monthlyRevenue = Number(txSums?.totalIncome || 0);
    const monthlyExpenses = Number(txSums?.totalExpense || 0);

    // Generate 12-month projections based on current vault data
    const projections = generateProjections(
      monthlyRevenue,
      monthlyExpenses,
      profile.operatingMode || 'stable',
      totalVault
    );

    await db.update(artistBusinessPlans).set({
      linkedEngineMode: profile.operatingMode,
      projections,
      updatedAt: new Date(),
    }).where(eq(artistBusinessPlans.artistId, artistId));

  } catch (error) {
    console.error(`[Bridge] Error syncing engine to business plan for artist ${artistId}:`, error);
  }
}

