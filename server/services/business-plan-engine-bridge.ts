/**
 * Checks artist activity and auto-updates milestone statuses.
 * Should be called periodically (e.g., every hour or after key actions).
 */
export async function updateDynamicRoadmap(artistId: number): Promise<{ updated: string[] }> {
  const updatedMilestones: string[] = [];

  try {
    const [plan] = await db.select().from(artistBusinessPlans)
      .where(eq(artistBusinessPlans.artistId, artistId)).limit(1);

    if (!plan || !plan.roadmapAutoUpdate) return { updated: [] };

    const milestones: RoadmapMilestone[] = (plan.milestones as RoadmapMilestone[]) || [];
    const executionLog: ExecutionLogEntry[] = (plan.roadmapExecutionLog as ExecutionLogEntry[]) || [];
    const now = new Date();

    // Get artist activity data
    const [songCount] = await db.select({ count: count() }).from(songs)
      .where(eq(songs.userId, artistId));

    const [vault] = await db.select().from(artistTreasuryVault)
      .where(eq(artistTreasuryVault.artistId, artistId)).limit(1);

    const [profile] = await db.select().from(artistEconomicProfile)
      .where(eq(artistEconomicProfile.artistId, artistId)).limit(1);

    const totalSongs = Number(songCount?.count || 0);
    const totalVault = vault ? Number(vault.operationBalance || 0) +
      Number(vault.reserveBalance || 0) +
      Number(vault.growthBalance || 0) +
      Number(vault.defiBalance || 0) : 0;

    // Check each planned/in_progress milestone against actual data
    for (let i = 0; i < milestones.length; i++) {
      const m = milestones[i];
      if (m.status === 'completed') continue;

      const milestoneId = m.id || `milestone-${i}`;
      let shouldComplete = false;
      let shouldStartProgress = false;

      // Auto-detect milestone completion based on category
      switch (m.category) {
        case 'release':
        case 'music':
          // Check if songs target met
          const targetSongs = extractNumberFromTitle(m.title) || 5;
          if (totalSongs >= targetSongs) shouldComplete = true;
          else if (totalSongs >= targetSongs * 0.5) shouldStartProgress = true;
          break;

        case 'financial':
        case 'revenue':
          // Check vault balance targets
          const targetAmount = extractNumberFromTitle(m.title) || 1000;
          if (totalVault >= targetAmount) shouldComplete = true;
          else if (totalVault >= targetAmount * 0.5) shouldStartProgress = true;
          break;

        case 'engine':
        case 'economic':
          // Check if engine mode reached target
          if (profile?.isEnabled && m.title.toLowerCase().includes(profile.operatingMode || '')) {
            shouldComplete = true;
          }
          break;

        case 'growth':
          // Time-based milestones — check if date has passed
          if (new Date(m.date) <= now && m.status === 'in_progress') {
            shouldStartProgress = true; // Mark as needs review
          }
          break;
      }

      // Update milestone status
      if (shouldComplete && m.status !== 'completed') {
        milestones[i] = {
          ...m,
          id: milestoneId,
          status: 'completed',
          completedAt: now.toISOString(),
        };
        updatedMilestones.push(m.title);

        executionLog.push({
          milestoneId,
          event: 'auto_completed',
          plannedDate: m.date,
          actualDate: now.toISOString(),
          delta: Math.round((now.getTime() - new Date(m.date).getTime()) / (1000 * 60 * 60 * 24)),
          details: `Milestone "${m.title}" auto-completed by system`,
          timestamp: now.toISOString(),
        });

        // Generate news for completed milestone
        await generateMilestoneNews(artistId, m);
      } else if (shouldStartProgress && m.status === 'planned') {
        milestones[i] = { ...m, id: milestoneId, status: 'in_progress' };

        executionLog.push({
          milestoneId,
          event: 'auto_started',
          plannedDate: m.date,
          details: `Milestone "${m.title}" auto-started — progress detected`,
          timestamp: now.toISOString(),
        });
      }
    }

    // Auto-generate new milestones if all current ones are completed
    const activeMilestones = milestones.filter(m => m.status !== 'completed');
    if (activeMilestones.length === 0 && milestones.length > 0) {
      const newMilestones = generateNextPhaseMilestones(
        profile?.operatingMode || 'stable',
        totalSongs,
        totalVault
      );
      milestones.push(...newMilestones);
    }

    // Save updates
    if (updatedMilestones.length > 0 || activeMilestones.length === 0) {
      await db.update(artistBusinessPlans).set({
        milestones,
        roadmapExecutionLog: executionLog.slice(-100), // Keep last 100 entries
        lastRoadmapSync: now,
        updatedAt: now,
      }).where(eq(artistBusinessPlans.artistId, artistId));
    }

    return { updated: updatedMilestones };
  } catch (error) {
    console.error(`[Bridge] Error updating roadmap for artist ${artistId}:`, error);
    return { updated: [] };
  }
}

