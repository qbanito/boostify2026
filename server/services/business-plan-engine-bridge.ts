interface RoadmapMilestone {
  id?: string;
  title: string;
  date: string;
  category: string;
  status: 'planned' | 'in_progress' | 'completed';
  description?: string;
  completedAt?: string;
  autoGenerated?: boolean;
  linkedToEngine?: boolean;
  priority?: 'low' | 'medium' | 'high' | 'critical';
}

interface ExecutionLogEntry {
  milestoneId: string;
  event: string;
  plannedDate: string;
  actualDate?: string;
  delta?: number;
  details?: string;
  timestamp: string;
}

