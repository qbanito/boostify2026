/**
 * Returns combined data from both business plan and economic engine
 * for the artist dashboard integration view.
 */
export async function getCombinedDashboardData(artistId: number) {
  const [plan] = await db.select().from(artistBusinessPlans)
    .where(eq(artistBusinessPlans.artistId, artistId)).limit(1);
  const [profile] = await db.select().from(artistEconomicProfile)
    .where(eq(artistEconomicProfile.artistId, artistId)).limit(1);
  const [vault] = await db.select().from(artistTreasuryVault)
    .where(eq(artistTreasuryVault.artistId, artistId)).limit(1);

  return {
    businessPlan: plan || null,
    engineProfile: profile || null,
    vault: vault || null,
    isLinked: !!(plan && profile?.isEnabled),
    engineMode: profile?.operatingMode || 'none',
    totalVault: vault ? Number(vault.operationBalance || 0) +
      Number(vault.reserveBalance || 0) +
      Number(vault.growthBalance || 0) +
      Number(vault.defiBalance || 0) : 0,
    milestones: {
      total: ((plan?.milestones as RoadmapMilestone[]) || []).length,
      completed: ((plan?.milestones as RoadmapMilestone[]) || []).filter(m => m.status === 'completed').length,
      inProgress: ((plan?.milestones as RoadmapMilestone[]) || []).filter(m => m.status === 'in_progress').length,
      planned: ((plan?.milestones as RoadmapMilestone[]) || []).filter(m => m.status === 'planned').length,
    },
  };
}
