// ═══════════════════════════════════════════
// MAIN: Full Roadmap Scan & Update
// ═══════════════════════════════════════════

export async function runRoadmapScan(artistId: number): Promise<RoadmapScanResult> {
  console.log(`🔍 [RoadmapEngine] Running full scan for artist ${artistId}...`);

  // 1. Scan current profile state
  const snapshot = await scanArtistProfile(artistId);

  // 2. Get or create business plan
  let [plan] = await db.select().from(artistBusinessPlans)
    .where(eq(artistBusinessPlans.artistId, artistId)).limit(1);

  if (!plan) {
    [plan] = await db.insert(artistBusinessPlans).values({
      artistId,
      businessName: `${snapshot.artistName} Music`,
      roadmapAutoUpdate: true,
      milestones: [],
      roadmapExecutionLog: [],
    }).returning();
  }

  const milestones: RoadmapMilestone[] = (plan.milestones as RoadmapMilestone[]) || [];
  const executionLog: any[] = (plan.roadmapExecutionLog as any[]) || [];
  const result: RoadmapScanResult = {
    snapshot,
    milestonesUpdated: [],
    milestonesCreated: [],
    actionsExecuted: [],
    newsGenerated: [],
  };
  const now = new Date();

  // 3. Generate smart milestones based on profile gaps
  const newMilestones = generateSmartMilestones(snapshot, milestones);
  for (const m of newMilestones) {
    milestones.push(m);
    result.milestonesCreated.push(m.title);
  }

  // 4. Evaluate all milestones against current profile
  for (let i = 0; i < milestones.length; i++) {
    const m = milestones[i];
    if (m.status === 'completed') continue;

    // Update currentValue for tracking
    if (m.profileMetric) {
      milestones[i].currentValue = getMetricValue(m.profileMetric, snapshot);
    }

    const evaluation = evaluateMilestone(m, snapshot);

    if (evaluation === 'completed') {
      milestones[i] = {
        ...milestones[i],
        status: 'completed',
        completedAt: now.toISOString(),
      };
      result.milestonesUpdated.push(m.title);

      executionLog.push({
        milestoneId: m.id,
        event: 'auto_completed',
        plannedDate: m.date,
        actualDate: now.toISOString(),
        details: `"${m.title}" completed — profile scan detected target met`,
        timestamp: now.toISOString(),
      });

      // 5. Execute milestone action
      if (m.action && !m.newsPublished) {
        const actionResult = await executeMilestoneAction(m, snapshot);
        if (actionResult) {
          result.actionsExecuted.push(actionResult);
          milestones[i].action = { ...m.action, executedAt: now.toISOString(), result: actionResult };
          milestones[i].newsPublished = true;
          result.newsGenerated.push(m.title);
        }
      }

    } else if (evaluation === 'in_progress' && m.status === 'planned') {
      milestones[i] = { ...milestones[i], status: 'in_progress' };
      result.milestonesUpdated.push(`${m.title} (started)`);

      executionLog.push({
        milestoneId: m.id,
        event: 'auto_started',
        plannedDate: m.date,
        details: `"${m.title}" — progress detected`,
        timestamp: now.toISOString(),
      });
    }
  }

  // 6. Save everything
  await db.update(artistBusinessPlans).set({
    milestones,
    roadmapExecutionLog: executionLog.slice(-200),
    lastRoadmapSync: now,
    updatedAt: now,
  }).where(eq(artistBusinessPlans.artistId, artistId));

  console.log(
    `✅ [RoadmapEngine] Scan complete: ${result.milestonesCreated.length} created, ` +
    `${result.milestonesUpdated.length} updated, ${result.actionsExecuted.length} actions, ` +
    `${result.newsGenerated.length} news`
  );

  return result;
}

// ═══════════════════════════════════════════
// PERIODIC SCANNER — For all artists with auto-update enabled
// ═══════════════════════════════════════════

export async function runGlobalRoadmapScan(): Promise<{ scanned: number; updated: number }> {
  const plans = await db.select({ artistId: artistBusinessPlans.artistId })
    .from(artistBusinessPlans)
    .where(eq(artistBusinessPlans.roadmapAutoUpdate, true));

  let updated = 0;
  for (const { artistId } of plans) {
    try {
      const result = await runRoadmapScan(artistId);
      if (result.milestonesUpdated.length > 0 || result.milestonesCreated.length > 0) updated++;
    } catch (e: any) {
      console.error(`[RoadmapEngine] Global scan failed for artist ${artistId}:`, e.message);
    }
  }

  console.log(`[RoadmapEngine] Global scan: ${plans.length} scanned, ${updated} updated`);
  return { scanned: plans.length, updated };
}
